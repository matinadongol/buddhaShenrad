                    <p>TSADZIN LOPÖN GESHE
                        MÖNLAM WANGYAL
                        PUGYALWA is from the Pontsang clan which originated
                        from the Döng Lineage. The first Pontsang clan was from
                        Kham, Chamdo, Tibet. The clan had many great masters
                        who were meditators and practitioners of Dzogchen, such
                        as Lama Kunga Tsultrim, Lama Rinchen Tenzin, Pontsang
                        Thekchok Rinpoche, and Khandro Tshuga Rinpoche. The
                        Pontsang clan had seven generations of great artists whose
                        religious art and paintings followed the ancient traditional
                        Tibetan artistic canon. Lopön Mönlam Wangyal started
                        learning the Tibetan language at the age of seven and he
                        was schooled at home by his grandfather, Pontsang Takla,
                        for eight years. He was taught thangka painting by his
                        uncle, Pontsang Lodoe Gyurme (also a monk) who was a highly skilled and well known
                        artist of his time. After his uncle’s passing, he learned from another famous artist called,
                        Dhondo Tsering Yomphel, who is Yongdzin Lopön Tenzin Namdak Rinpoche’s uncle. </p>
                    <br>
                    <p>At the age of eighteen, Tsadzin Lopön Mönlam Wangyal became a monk at Thongdrol
                        Ritrö Monastery in Kham, Tibet. In 1992, he went to India and studied the Yungdrung
                        Bön Sutras, Philosophy, Astrology, Tibetan Grammar, and Poetry at Menri Monastery. In
                        1997, he came to Nepal and studied the Tantras, Dzogchen, Mandala Painting, Tibetan
                        Medicine, and Tibetan History at Triten Norbutse Monastery. He has acquired knowledge
                        on the Sciences of Buddhists learning, took responsibility of Disciplinarian/Gekhöd, and
                        Librarian for ten years after which he was honored with the title of “Tsadzin Lopön”.</p>
                    <br>
                    <p>Tsadzin Lopön began painting these Eighty Thangkas of the Great Accomplished
                        Ones of antiquity, Drubthob Chenpo/Mahasiddhas, in early 2009 and completed them in
                        2014. The main objective behind these paintings is to give a clear and simple explanation
                        of their true meaning in a new and original way to facilitate comprehension and
                        understanding.</p>
